import UIKit

public extension UIImage {
    class func ballImage() -> UIImage? {
        return UIImage(named: "Ball", in: .module, compatibleWith: nil)
    }
    class func scheduleImage() -> UIImage? {
        return UIImage(named: "Schedule", in: .module, compatibleWith: nil)
    }
}


